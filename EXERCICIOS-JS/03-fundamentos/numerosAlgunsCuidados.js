// Tipo infinity
console.log(7 / 0)

// JS converte e faz a operação mat.
console.log("10" / 2)

// Vai dar NOT A NUMBER (NaN) a conversão não permite uma operação mat.
console.log("Show!" * 2)

// Esse resultado é o tipo de operação de ponto flutuante que o JS suporta.
console.log(0.1 + 0.7)

// console.log(10.toString())
console.log((10.345).toFixed(2))

// Quando há soma a string concatena(junta os números) com o numeral
console.log('3' + 2) // string ganha! O sinal de + o JS interpreta como uma concatenação ao invés de somar.

// Quando há subtração a string subtrai normalmente o numeral.
console.log('10' - 2) // Nesse caso substituir faz sentido para o JS.

